# ifelse.py

# Программа, которая проверяет, является ли число четным или нечетным
num = int(input("Введите число: "))
if num % 2 == 0:
    print(f"{num} - четное")
else:
    print(f"{num} - нечетное")

# Программа, которая запрашивает два числа и выводит наибольшее
num1 = int(input("Введите первое число: "))
num2 = int(input("Введите второе число: "))
print(f"Наибольшее число: {max(num1, num2)}")

# Программа, которая проверяет, можно ли получить водительские права
age = int(input("Введите ваш возраст: "))
if age >= 18:
    print("Вы можете получить водительские права.")
else:
    print("Вам нужно подождать до 18 лет.")